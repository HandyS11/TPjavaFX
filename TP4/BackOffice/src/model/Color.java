package model;

public class Color {

    private int red;
    private int green;
    private int bleu;


    public Color(int red, int green, int bleu) {
        this.red = red;
        this.green = green;
        this.bleu = bleu;
    }


    public int getRed() {
        return red;
    }

    public int getGreen() {
        return green;
    }

    public int getBleu() {
        return bleu;
    }
}
